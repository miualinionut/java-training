package code._4_student_effort;

public class Student {
    String _name;
    int _money;

    public Student(String _name, int _money) {
        this._name = _name;
        this._money = _money;
    }

    public String get_name() {
        return _name;
    }

    public int get_money() {
        return _money;
    }
}
