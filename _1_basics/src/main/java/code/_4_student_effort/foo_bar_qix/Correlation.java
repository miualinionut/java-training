package code._4_student_effort.foo_bar_qix;

public class Correlation {
    private Integer number;
    private String word;

    public Correlation(Integer number, String word) {
        this.number = number;
        this.word = word;
    }

    public Integer getNumber() {
        return this.number;
    }

    public String getWord() {
        return this.word;
    }
}
