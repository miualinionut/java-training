package code._4_student_effort.fizz_buzz;

public class Correlation {
    private Integer number;
    private String word;

    public Correlation (Integer number, String word) {
        this.number = number;
        this.word = word;
    }
    public Integer getNumber() {
        return this.number;
    }
    public String getWord() {
        return this.word;
    }
}
